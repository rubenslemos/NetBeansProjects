    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        int n = Integer.parseInt(numerador.getValue().toString());
        double resto = n%2;
        lblRD2Valor.setText(Double.toString(resto));
        double cubo = Math.pow(n, 3);
        lblCuboValor.setText(Double.toString(cubo));
        double quad = Math.sqrt(n);
        lblQuadValor.setText(Double.toString(Math.round(quad)));
        double cubica = Math.cbrt(n);
        lblCubicaValor.setText(Double.toString(Math.round(cubica)));
        int abs = Math.abs(n);
        lblValAbsValor.setText(Integer.toString(abs));
        panCalc.setVisible(true);
        
    }//GEN-LAST:event_btnCalcularActionPerformed

